function make_isotropic_niis( data_dir, R, train_bool, use_bias_field )
%MAKE_ISOTROPIC_NIIS Summary of this function goes here
%   Detailed explanation goes here
    verbose = false;
    nii_ext = {'*.nii; *.hdr; *.img; *.nii.gz'};

    %reslice 20s image to be isotropic
    reslice_nii(try_find_file(data_dir,'**/20s.nii*'),...
        [data_dir,'/temp/20s_isotropic.nii'],...
        [R,R,R], verbose);

    %reslice pre image to be isotropic
    reslice_nii(try_find_file(data_dir,'**/pre_reg.nii*'),...
        [data_dir,'/temp/pre_reg_isotropic.nii'],...
        [R,R,R], verbose);

    %reslice 70s to be isotropic 
    reslice_nii(try_find_file(data_dir,'**/70s_reg.nii*'),...
        [data_dir,'/temp/70s_reg_isotropic.nii'],...
        [R,R,R], verbose);

    %reslice whole liver segmentation to be isotropic 
    reslice_nii([data_dir,'/temp/whole_liver.nii'],...
        [data_dir,'/temp/whole_liver_isotropic.nii'],...
        [R,R,R], verbose);

    %reslice t2 to be isotropic 
    reslice_nii(try_find_file(data_dir,'**/t2_bfc_reg.nii*',...
                        'Select the T2 nifti file', nii_ext),...
        [data_dir,'/temp/t2_bfc_reg_isotropic.nii'],...
        [R,R,R], verbose);

    if use_bias_field
        %reslice t1 image bias field estimate to be isotropic 
        reslice_nii(try_find_file(data_dir,'**/bias*.nii*',...
                        'Select the T1 bias field estimate', nii_ext),...
            [data_dir,'/temp/bias_field_isotropic.nii'],...
            [R,R,R], verbose);
    end

    if train_bool
        %reslice vessel segmentation to be isotropic 
        reslice_nii([data_dir,'/temp/vessel.nii'],...
            [data_dir,'/temp/vessel_isotropic.nii'],...
            [R,R,R], verbose);

        %reslice necrosis segmentation to be isotropic 
        reslice_nii([data_dir,'/temp/necrosis.nii'],...
            [data_dir,'/temp/necrosis_isotropic.nii'],...
            [R,R,R], verbose);

        %reslice tumor segmentation to be isotropic 
        reslice_nii([data_dir,'/temp/tumor.nii'],...
            [data_dir,'/temp/tumor_isotropic.nii'],...
            [R,R,R], verbose);
    end

end

